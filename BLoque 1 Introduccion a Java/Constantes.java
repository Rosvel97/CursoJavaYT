public class Constantes {
    public static void main(String[] args) {
    //Se hacen uso de las constantes, con la palabra reservada Final
    //Las constantes no pueden cambiar su valor como las variables
    final String nombre="David Rosales";
    final int edad=25;
    final String correo="Sandead176@gmail.com";
    final int carnet=1706712022;
    
    System.out.println("Nombre "+nombre);
    System.out.println("Edad "+edad);
    System.out.println("Correo "+correo);
    System.out.println("Carnet "+carnet);
    }
}
