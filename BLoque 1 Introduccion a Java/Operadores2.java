//Clase 7
//Operadores incremento ++ y decremento--
//Tambien llamados como contadores
public class Operadores2 {
    public static void main(String[] args) {
       int num=5;
       num++; //num+1; Incremento
       System.out.println("El incremento es "+num); 
       num--; //num-1; Decremento
       System.out.println("El decremento es "+num);
    }
}
